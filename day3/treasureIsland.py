which_direction = input("You''re at a crossroad. Where do you want to go? Type left or right.\n")
which_direction = which_direction.lower()

if which_direction == "right":
  print("Game over. You fell off a cliff.")
else:
  swim_or_wait = input("Swim or wait?\n")
  swim_or_wait = swim_or_wait.lower()
  if swim_or_wait == "swim":
    print("Game over.")
  else:
    door_color = input("Which color door do you choose?\n")
    door_color = door_color.lower()
    if door_color == "red":
      print("Burned. Game over.")
    elif door_color == "blue":
      print("Eaten by beasts. Game over.")
    elif door_color == "yellow":
      print("You win!!")
    else:
      print("Game over!")
